cat > ../cmd/sharehodld/main_simple.go << 'EOF'
package main

import (
	"os"

	"cosmossdk.io/log"
	svrcmd "github.com/cosmos/cosmos-sdk/server/cmd"

	"github.com/sharehodl/sharehodl-blockchain/app"
	"github.com/sharehodl/sharehodl-blockchain/cmd/sharehodld/cmd"
)

func main() {
	rootCmd := cmd.NewRootCmd()
	if err := svrcmd.Execute(rootCmd, "SHAREHODL", app.DefaultNodeHome); err != nil {
		log.NewLogger(os.Stderr).Error("failure when running app", "err", err)
		os.Exit(1)
	}
}
EOF

